import turtle as t
t.title('Making a circle.')
t.speed(10)
t.pensize(100)
t.pencolor('orchid')
t.bgcolor('black')
t.shape('turtle')
t.fillcolor("grey")
t.begin_fill()
t.circle(130)
t.end_fill()
t.done()



