import turtle as c
c.bgcolor('blue')
c.title('My car')
c.fillcolor('pink')
c.begin_fill()
for i in range(2):
    c.fd(200)
    c.rt(90)
    c.fd(30)
    c.rt(90)
c.end_fill()
c.fd(50)
c.lt(45)
c.fd(50)
c.rt(45)
c.fd(80)
c.rt(45)
c.fd(50)
c.rt(135)
c.fd(55)
c.rt(90)
c.fd(40)
c.pu()
c.goto(50,-30)
c.pd()
c.fillcolor('black')
c.begin_fill()
c.circle(10)
c.end_fill()
c.pu()
c.goto(150,-30)
c.pd()
c.fillcolor('black')
c.begin_fill()
c.circle(10)
c.end_fill()
c.pu()
c.goto(150,-70)
c.pd()
c.write('My car',font=('Airal',16,'bold'))
c.ht()
c.st()
